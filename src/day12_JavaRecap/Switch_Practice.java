package day12_JavaRecap;

public class Switch_Practice {
    public static void main(String[] args) {

        switch (3){
            case 1:
                System.out.println("Case 1");
                break;

            case 2:
                System.out.println("Case 2");
                break;

                default:
                    System.out.println("Invalid case");

        }

    }

}
