package day18_NestedLoop;

public class doWhileLoop {
    public static void main(String[] args) {

        int i = 5;

        do {

            System.out.println("Hello World");
            i--;

        }while (i == 0);    // false

        System.out.println("=============================");

        while (i == 0){
            System.out.println("Hello Cybertek");
        }

    }
}
