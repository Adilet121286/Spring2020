package Repl_it_Practice;

import java.util.Scanner;

public class Replit_loopDrawRect {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int n = s.nextInt();

        for (int i = 1; i<=3; i++){
            System.out.print("x");
        }

        System.out.println();

        for (int j = n; j>0; j--){
            System.out.println("x x");
        }

        for (int k = 1; k<=3; k++){
            System.out.print("x");
        }






    }
}
