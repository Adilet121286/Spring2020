package day02_Veriables;

public class StringOfText {
    public static void main(String[]args){

        String employeeName = "Adilet";
        String companyName = "Cybertek";

        System.out.println("Adilet");

        System.out.println(employeeName);
        /*
        Employee name is Adilet
        Cmpany name is Bank of America
        Adilet works at Bank of America
         */

        System.out.println("Employee name is: "+employeeName);
        System.out.println("Company name is: "+companyName);
        System.out.println(employeeName+" works at "+companyName);
        System.out.println("=========================");
        System.out.println("Employee name is: "+employeeName + "\nCompany name is:"+companyName+"\n"+employeeName
                            +" works at"+companyName);

        //psvm



    }
}
